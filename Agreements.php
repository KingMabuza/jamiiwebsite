                    <h3>Regional integration in Africa</h3>
                    <p><a href=""><img
                                src="https://github.com/AfCFTA/images/blob/main/map_Africa_REC_overlay.png?raw=true"
                                width="100" height="140">
                        </a>
                        The African Union remains committed to implementing its regional integration agenda through its
                        8 Regional Economic Communities (RECs): COMESA, EAC, and SADC in the east and southern Africa
                        region, along with ECCAS, ECOWAS, IGAD, AMU, and CEN-SAD covering Central, West and Northern
                        Africa. Although some significant progress has been made to date, the multiplicity of
                        inter-governmental organisations in Africa (currently 14) and overlapping memberships in the
                        RECs poses a challenge for African integration and the implementation of regional agreements. In
                        addition, a COMESA-EAC-SADC Tripartite Free Trade Area (TFTA) has been concluded, and the
                        African Continental Free Trade Area (AfCFTA) Agreement – covering all 55 AU member states – was
                        signed in March 2018. Start of trading under the AfCFTA Agreement began on 1 January 2021. Click
                        on the maps alongside to find out more.
                    </p>
                </div>

                <div id="COMESA" class="tabcontent">

                    <h3>COMESA</h3>
                    <p><a href=""><img
                                src="https://github.com/AfCFTA/images/blob/main/comesa-mapchart-2021.png?raw=true"
                                width="100" height="140">
                        </a>
                        The Common Market for Eastern and Southern Africa (COMESA) was established on 8 December 1994,
                        replacing the Preferential Trade Area for Eastern and Southern Africa which was set up in 1981.
                        COMESA has adopted a developmental approach to regional integration, with its main objective
                        being the formation of a large economic and trading unit capable of overcoming some of the
                        barriers that individual states face. COMESA launched a Free Trade Area on 31 October 2000
                        followed by a Customs Union in June 2009, although the latter is yet to become operational.
                        COMESA is the largest regional economic community in Africa with 21 member states: Burundi,
                        Comoros, Djibouti, DRC, Egypt, Eritrea, Ethiopia, Kenya, Libya, Madagascar, Malawi, Mauritius,
                        Rwanda, Seychelles, Somalia, Sudan, Swaziland, Tunisia, Uganda, Zambia and Zimbabwe. COMESA has
                        concluded a free trade agreement with the United States and several members are negotiating an
                        Economic Partnership Agreement with the EU as part of the Eastern and Southern Africa (ESA) EPA
                        group.
                    </p>
                </div>

                <div id="EAC" class="tabcontent">
                    <h3>EAC</h3>
                    <p><a href=""><img src="https://github.com/AfCFTA/images/blob/main/eac-mapchart-2021.png?raw=true"
                                width="100" height="140"></a>
                        The East African Community (EAC) was established by Treaty as a regional intergovernmental
                        organisation on 7 July 2000. Regional trade integration is a cornerstone of EAC Members’ trade
                        policies. EAC Partner States have agreed to coordinate their economic and other policies with a
                        view to creating conditions favourable for the development and achievement of the objectives of
                        the Community – accelerated, harmonious and balanced development and sustained expansion of
                        economic activities for their mutual benefit. The first regional integration milestone, the EAC
                        Customs Union, was successfully launched in 2005, followed by the establishment of the Common
                        Market in 2010 and implementation of the East African Monetary Union Protocol in 2012. The EAC
                        is working towards establishing a Political Federation of the East African States . The EAC
                        currently consists of six Member States: Burundi, Kenya, Republic of South Sudan (admitted in
                        April 2016), Rwanda, United Republic of Tanzania, and Uganda. The EAC has concluded free trade
                        agreements with the United States and an Economic Partnership Agreement with the EU.
                    </p>
                </div>

                <div id="SADC" class="tabcontent">
                    <h3>SADC</h3>
                    <p><a href=""><img src="https://github.com/AfCFTA/images/blob/main/sadc-mapchart-2021.png?raw=true"
                                width="100" height="140"></a>
                        The Southern African Development Community (SADC) was established on 17 August 1992 with the
                        adoption of the SADC Treaty, redefining the existing Southern African Development Coordinating
                        Conference which was established in 1980. The Treaty sets out the main objectives of SADC – to
                        achieve development and economic growth, alleviate poverty, enhance the standard and quality of
                        life of the peoples of Southern Africa, and support the socially disadvantaged through regional
                        integration. To this end, SADC launched its Free Trade Area in August 2008 as agreed upon by the
                        member states in the SADC Trade Protocol. Further ambitious integration objectives are contained
                        in the Regional Indicative Strategic Development Plan of 2003. Although it is not a legally
                        binding agreement, it enjoys political legitimacy. A revised RISDP (2015-2020) was published by
                        SADC in 2017. SADC currently consists of 16 member states: Angola, Botswana, the Democratic
                        Republic of the Congo, Lesotho, Madagascar, Malawi, Mozambique, Mauritius, Namibia, Seychelles,
                        South Africa, Swaziland, Tanzania, Union of Comoros, Zambia and Zimbabwe. SADC has negotiated an
                        Economic Partnership Agreement with the EU.
                    </p>
                </div>

                <div id="SACU" class="tabcontent">
                    <h3>SACU</h3>
                    <p><a href=""><img src="https://github.com/AfCFTA/images/blob/main/sacu-mapchart-2021.png?raw=true"
                                width="100" height="140">
                        </a>
                        The Southern African Customs Union (SACU) was established in 1910 between Botswana, Lesotho,
                        Eswatini and South Africa. It is the oldest Customs Union in existence. The SACU Agreement was
                        renegotiated in 1969 when Namibia gained independence from South Africa and became a contracting
                        party to the Agreement. It was subsequently renegotiated amongst the five member countries in
                        the mid-1990s which culminated in the signing of the new SACU Agreement in October 2002. Annex 1
                        of the 2002 Agreement details the Revenue Sharing Formula that is used for determining each
                        Member State’s share out of the Common Revenue Pool. The implementation of the new Revenue
                        Sharing Formula began in 2006. Additionally, SACU has concluded free trade agreements with the
                        United States and the European Free Trade Association (EFTA), as well as negotiating an Economic
                        Partnership Agreement with the EU.
                    </p>
                </div>

                <div id="TFTA" class="tabcontent">
                    <h3>TFTA</h3>
                    <p><a href=""><img src="https://github.com/AfCFTA/images/blob/main/tfta-mapchart-2021.png?raw=true"
                                width="100" height="140">
                        </a>
                        The member states of COMESA, EAC and SADC agreed in October 2008 to negotiate a Tripartite Free
                        Trade Area (TFTA) with the aim of bringing together, in one common market, countries in the
                        three regional economic blocs. It will cover a combined population of 700 million people with an
                        estimated GDP of over US$1.4 trillion. The TFTA was officially launched in Sharm El Sheikh,
                        Egypt on 10 June 2015 where Heads of State and Government had met to conclude outstanding
                        negotiations on rules of origin, trade remedies and tariff offers. However, due to a number of
                        challenges faced in the process, the deadline of June 2016 was not met, and the commencement of
                        Phase II negotiations – covering trade in services and other trade related matters – was
                        delayed. As at February 2020, all Annexes to the Agreement have been finalised, and the
                        negotiation of rules of origin and exchange of tariff offers is near completion. The TFTA
                        Agreement has been signed by 22 of the 27 member countries, which will enter into full force
                        once it has been ratified by 14 countries. To date, it has been both signed and ratified by
                        eight countries – Burundi, Kenya, Egypt, Rwanda, Uganda, South Africa, Namibia and Botswana.
                    </p>
                </div>
                <div id="AfCFTA" class="tabcontent">
                    <h3>AfCFTA</h3>
                    <p><a href=""><img
                                src="https://github.com/AfCFTA/images/blob/main/afcfta-mapchart-2021.png?raw=true"
                                width="100" height="140">
                        </a>
                        In January 2012, the 18th AU Assembly endorsed a comprehensive framework towards the attainment
                        of developmental regionalism in the continent, through its decision to establish an African
                        Continental Free Trade Area (AfCFTA). The main objectives of the AfCFTA are to create a single
                        continental market for goods and services, with free movement of business persons and
                        investments. Once implemented, the AfCFTA will cover a market of more than 1.2 billion people
                        with a combined GDP of more than US$3.4 trillion. The consolidated text of the AfCFTA Agreement
                        was signed by 44 of the 55 AU member states on 21 March 2018 in Kigali, Rwanda. Only Eritrea’s
                        signature is now outstanding. The Agreement Establishing the AfCFTA entered into force on 30 May
                        2019, while the operational phase of the Agreement commenced on 7 July 2019. The AfCFTA will be
                        governed by five operational instruments, i.e. the Rules of Origin; the online negotiating
                        forum; the monitoring and elimination of non-tariff barriers; a digital payments system and the
                        African Trade Observatory. Trade under the AfCFTA Agreement began on 1 January 2021. As at June
                        2021, the Agreement has been ratified by 37 countries.
                    </p>
                </div>

                <div id="African Union resources" class="tabcontent">
                    <h3>African Union resources</h3>
                    <p><a href=""><img
                                src="https://github.com/AfCFTA/images/blob/main/african-union-mapchart-2021.png?raw=true"
                                width="100" height="140">
                        </a>
                        With a view to accelerating the process of economic and political integration in the continent,
                        the Constitutive Act of the African Union was adopted in 2000 at the Lomé Summit and entered
                        into force in 2001. The vision for the AU is to support the empowerment of African states in the
                        global economy while addressing the multifaceted social, economic and political problems facing
                        the continent amidst increasing globalisation. With the adoption of the Treaty Establishing the
                        African Economic Community in 1991 (the ‘Abuja Treaty’), the AU is working towards the
                        establishment of a single African Common Market through six successive stages, using the
                        Regional Economic Communities (RECs) as building blocks. Agenda 2063 was formally adopted in
                        January 2015 as a strategic framework for the socioeconomic transformation of the continent over
                        the next 50 years. It encapsulates not only Africa’s aspirations for the future but also
                        identifies key Flagship Programmes which can boost Africa’s economic growth and development and
                        lead to the rapid transformation of the continent. The AU currently consists of 55 member
                        states.
                    </p>
                </div>

                <div id="Africas external relations" class="tabcontent">
                    <h3>Africas external relations</h3>
                    <p><a href=""><img
                                src="https://github.com/AfCFTA/images/blob/main/africa-external-relations-globe_v2.png?raw=true"
                                width="100" height="140">
                        </a>
                        The challenges of the evolving international trade landscape and global economy, and how trade
                        policy in the 21st century can be used to promote the effective integration and upgrading of
                        developing countries, have meant that the nature of Africa’s trading relationships has been
                        undergoing significant shifts. While trade with its traditional trading partners, particularly
                        the European Union and the United States, has been declining, trade ties with emerging economies
                        and global players, notably the BRICS (Brazil, Russia, India, China and South Africa), has been
                        steadily increasing. The negotiation of Economic Partnership Agreements (EPAs) with the EU, the
                        United Kingdom’s exit from the EU (Brexit), and multilateral trade developments at the World
                        Trade Organisation (WTO) also have implications for Africa’s trade. Click on the map below to
                        find out more.
                    </p>
